// Literals are representations of fixed values in a program.
// 1. Integer Literals
var a = 10
print(a)

var b = 0b101
print(b)

var c = 0o13
print(c)

var d = 0x13
print(d)

// 2. Floating-point Literals
var f : Float = 3.14
print(f)

// 3. Boolean Literals
var g : Bool = true;
print(g)

// 4 . String and Character Literals
let char : Character = "A"
print(char)

let name : String = "Aasim"
print(name)